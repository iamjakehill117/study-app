乱流の難しさは、流れのスケールが一つではないことにあります。大きな渦は形状や境界条件からエネルギーを受け取り、そのエネルギーをより小さな渦へ渡します。最終的に、非常に小さなスケールで粘性によって運動エネルギーが熱へ散逸します。

